class Solution{
public:	
	/* Function to replace every element with the
	next greatest element */
	void nextGreatest(int arr[], int n) {
	    // code here
	    int maxe = arr[n-1], j = n-2;
	    arr[n-1] = -1;
	    for(;j>= 0; j--){
	        int t = arr[j];
	        arr[j] = maxe;
	        maxe = max(maxe, t);
	    }
	}


};

